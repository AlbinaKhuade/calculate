
def summ(a:int, b:int)-> int:
    return a + b

def diff(a:int, b:int)-> int:
    return a - b

def multi(a:int, b:int)-> int:
    return a * b

def int_div(a:int, b:int)-> int:
    return a / b

def remain_div(a:int, b:int)-> int:
    return a % b

def exponent(a:int, b:int)-> int:
    return a**b

def square_root(a:int)-> int:
    return a ** 0.5

